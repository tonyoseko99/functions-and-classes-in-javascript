// Sample Lambda Function in JavaScript

const addNumbers = (a, b) => {
    return a + b;
};

const multiplyNumbers = (c, d) => {
    return c * d;
};

const divideNumbers = (e, f) => {
    return e / f;
}
// Example usage
console.log(addNumbers(5, 3)); // Output: 8
console.log(multiplyNumbers(6, 4));
console.log(divideNumbers(15, 5));
